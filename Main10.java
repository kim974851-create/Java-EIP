public class Main10 {
    public static void main(String[] args) {

        System.out.println(2 + 5 + 7);
        // 전부 int → 숫자 덧셈
        // 2 + 5 = 7, 7 + 7 = 14
        // 출력: 14

        System.out.println("2" + 5 + 7);
        // 왼쪽부터 평가
        // "2" + 5  → 문자열 + int → "25"  (문자열 연결)
        // "25" + 7 → 문자열 + int → "257" (문자열 연결)
        // 출력: 257

        System.out.println(2 + 5 + "7");
        // 왼쪽부터 평가
        // 2 + 5    → int + int    → 7     (숫자 덧셈)
        // 7 + "7"  → int + 문자열 → "77"  (문자열 연결)
        // 출력: 77

        System.out.println(2 + 5 + "7" + 8 + 9);
        // 왼쪽부터 평가
        // 2 + 5      → int + int    →  7     (숫자 덧셈)
        // 7 + "7"    → int + 문자열 → "77"   (문자열 연결)
        // "77" + 8   → 문자열 + int → "778"  (문자열 연결)
        // "778" + 9  → 문자열 + int → "7789" (문자열 연결)
        // 출력: 7789
    }
}

/*
 * 최종 출력
 * ──────────
 * 14
 * 257
 * 77
 * 7789
 *
 * 핵심 규칙
 * ──────────────────────────────────
 * + 연산은 왼쪽부터 순서대로 평가
 * int + int      → 숫자 덧셈
 * String + int   → 문자열 연결
 * int + String   → 문자열 연결
 * 문자열이 한번 등장하면 그 이후는 전부 문자열 연결
 */