public class Main12 {
    public static void main(String[] args) {

        System.out.println(2 + 3 + "4");
        // 왼쪽부터 평가
        // 2 + 3    → int + int    → 5    (숫자 덧셈)
        // 5 + "4"  → int + String → "54" (문자열 연결)
        // 출력: 54

        System.out.println("2" + 3 * 4);
        // * 가 + 보다 우선순위 높음 → 곱셈 먼저
        // 3 * 4    → int * int    → 12
        // "2" + 12 → String + int → "212" (문자열 연결)
        // 출력: 212

        System.out.println("2" + (3 + 4));
        // () 안을 먼저 계산
        // (3 + 4)  → int + int    → 7
        // "2" + 7  → String + int → "27" (문자열 연결)
        // 출력: 27
    }
}

/*
 * 최종 출력
 * ──────────
 * 54
 * 212
 * 27
 *
 * 핵심 규칙
 * ──────────────────────────────────
 * 연산 우선순위: () > * / > + -
 * 문자열 만나기 전까지는 숫자 연산
 * 문자열 만나는 순간부터 문자열 연결
 */